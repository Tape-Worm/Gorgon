		#region Value Types.
		/// <summary>
		/// Error code.
		/// </summary>
		internal struct D3DError
		{
			/// <summary>
			/// HRESULT code.
			/// </summary>
			internal long HResult;
			/// <summary>
			/// Text code.
			/// </summary>
			internal string Code;
			/// <summary>
			/// Description.
			/// </summary>
			internal string Description;

			/// <summary>
			/// Initializes a new instance of the <see cref="D3DError"/> struct.
			/// </summary>
			/// <param name="hresult">The hresult.</param>
			/// <param name="code">The code.</param>
			/// <param name="desc">The desc.</param>
			internal D3DError(long hresult, string code, string desc)
			{
				HResult = hresult;
				Code = code;
				Description = desc;
			}
		}
		#endregion

		#region Variables.
		private static readonly IDictionary<long, D3DError> _errors;
		#endregion

		#region Methods.
		/// <summary>
		/// Function to add a new error code.
		/// </summary>
		/// <param name="hresult">HRESULT code.</param>
		/// <param name="code">Text code.</param>
		/// <param name="desc">Description.</param>
		private static void Add(long hresult, string code, string desc)
		{
			_errors.Add(hresult, new D3DError(hresult, code, desc));
		}

		/// <summary>
		/// Function to retrieve an error based on its error code.
		/// </summary>
		/// <param name="code">Error code to look up.</param>
		/// <returns>The error code info.</returns>
		public static D3DError GetError(long code)
		{
		    D3DError result;

		    if (!_errors.TryGetValue(code, out result))
		    {
		        result = new D3DError(code, code.ToString(CultureInfo.InvariantCulture), "Unknown");
		    }

		    return result;
		}
	    #endregion

